package com.isecinc.pens.bean;

public class SalesrepBean {
   
	private String salesrepId;
	private String code;
	private String salesChannel;
	private String salesChannelName;
	private String region;
	private String regionName;
	private String salesrepFullName;
	public String getSalesrepId() {
		return salesrepId;
	}
	public void setSalesrepId(String salesrepId) {
		this.salesrepId = salesrepId;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getSalesChannel() {
		return salesChannel;
	}
	public void setSalesChannel(String salesChannel) {
		this.salesChannel = salesChannel;
	}
	public String getSalesChannelName() {
		return salesChannelName;
	}
	public void setSalesChannelName(String salesChannelName) {
		this.salesChannelName = salesChannelName;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public String getRegionName() {
		return regionName;
	}
	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}
	public String getSalesrepFullName() {
		return salesrepFullName;
	}
	public void setSalesrepFullName(String salesrepFullName) {
		this.salesrepFullName = salesrepFullName;
	}
	
	
}
